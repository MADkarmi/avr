void disp(unsigned char tab[])
{
	int delay =3000; //def val =3
	for(int8_t c = 0;c<3;c++){
		PORTB = tab[c];
		_delay_ms(delay); 
	}
	PORTB = 0x00;
	_delay_ms(delay);
}

